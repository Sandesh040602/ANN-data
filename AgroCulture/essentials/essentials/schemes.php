                    wastage of water, enhance crop per drop by implementing water-saving technologies and precision
                    irrigation.
                </li>
                <li>The goal is to open the doors for optimal water budgeting in all sectors. </li>
                <li> Convergence of investment in irrigation at the field level</li>
                <li>Enhance the adoption of being precise in irrigation and other water saving technologies (more crop
                    per drop)</li>

            </ul>
