        <div class="para1">
            <p><strong>THE FEATURES OF THIS SCHEME ARE : </strong></p>
            <br>
            <ul>
                <li>To meet short term credit requirements for cultivation of crops/dairy/animals/fisheries/poultry birds/other small ruminants.
                </li>
                <li>To provide Post-harvest expenses and produce Marketing loans.
                </li>
                <li>To meet consumption requirement of farmers.</li>
                <li>To meet working capital requirement for maintenance of farm equipments and other assets and activities allied to agriculture like dairy, fisheries etc.
                </li>

            </ul>
            <br>
            <br>
        </div>
        <div class = "para1">
        <p><strong>WHICH SECTION OF FARMERS ARE COVERED IN THIS?</strong></p>
        <br>
        <ul>
            <li>All Farmers-Individuals / joint borrowers who are owner cultivators, tenant farmers, oral lessees , share croppers and Self Help Groups or joint liability group of Farmers, Farmers Producer organisations.</li>
        </ul>
        <br>
        <br>
        </div>
        <div class = "para1">
            <p><strong>NATURE OF THE CREDIT GIVEN :</strong></p>
            <br>
            <ul>
                <li>Short-term revolving credit .</li>
            </ul>
            <br>
            <br>
            </div>
        <div class="para1">
            <p><strong>DETAILED OVERVIEW OF THE PROGRAM: </strong></p>
            <br>
            <ul>
                <li>Its objective is to meet the comprehensive credit requirements of the agriculture sector and by 2019 for fisheries and animal husbandry by giving financial support to farmers. Participating institutions include all commercial banks, Regional Rural Banks, and state co-operative banks.</li>
                <li>The scheme entails short-term credit for crops and term loans. KCC credit holders are also covered under personal accident insurance up to ₹50,000 for death and permanent disability, and up to ₹25,000 for other risks.
                </li>
                <li>The premium is borne by both the bank and the borrower in a 2:1 ratio. The validity period is five years, with an option to extend it up to three more years.</li>
                <li>Every farmer will be provided Rs. 20,000 per acre in three years for seed to harvesting of crops and
                    to transport produce to the market.
                    </li>
                <li> Kisaan Credit Card (KCC) credit to the farmers is of two types, viz., 1. Cash Credit (for working capital) and 2. Term Credit (for capital expenditure such as the purchase of cattle, pump sets, land development, plantation, drip irrigations, etc.
                </li>
            </ul>
