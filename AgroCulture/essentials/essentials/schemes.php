        <div class="para1">
            <p><strong>THE PROCESS OF IMPLEMENTATION OF THE PROGRAM: </strong></p>
            <br>
            <ul>
                <li>Groups of farmers would be motivated to take up organic farming under Paramparagat Krishi Vikas
                    Yojana (PKVY).</li>
                <li>Fifty or more farmers will form a cluster having 50 acre land to take up the organic farming under
                    the scheme. In this way during three years 10,000 clusters will be formed covering 5.0 lakh acre
                    area under organic farming.
                </li>
                <li>There will be no liability on the farmers for expenditure on certification.</li>
                <li>Every farmer will be provided Rs. 20,000 per acre in three years for seed to harvesting of crops and
                    to transport produce to the market.
                    Organic farming will be promoted by using traditional resources and the organic products will be
                    linked with the market</li>
                <li>It will increase domestic production and certification of organic produce by involving farmers
                </li>
            </ul>
