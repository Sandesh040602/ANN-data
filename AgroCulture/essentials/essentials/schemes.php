    <div class="container">
        <p class="ada"><strong>PRADHAN MANTRI FASAL BIMA YOJNA</strong></p>
        <br>
        <br>
        <img src="c3ac2bfa-d9d4-11eb-a5ce-a68058c7c394_1625079441804.webp" class="logo1" class="center">
        <br>
        <br>
        <div class="para1">
            <p><strong>THE FEATURES OF THIS SCHEME ARE : </strong></p>
            <br>
            <ul>
                <li>The Pradhan Mantri fasal bima yojana (PMFBY) launched on 18 February 2016 by Prime Minister Narendra
                    Modi is an insurance service for farmers for their yields.
                </li>
                <li>PMFBY aims to provide a comprehensive insurance cover against failure of the crop thus helping in
                    stabilising the income of the farmers. </li>
                <li> The Scheme covers all Food & Oilseeds crops and Annual Commercial/Horticultural Crops for which
                    past yield data is available and for which requisite number of Crop Cutting Experiments (CCEs) are
                    being conducted under General Crop Estimation Survey (GCES).</li>
                <li>Ensuring flow of credit to the agriculture sector which will contribute to food security, crop
                    diversification and enhancing growth and competitiveness of agriculture sector besides protecting
                    farmers from production risks.</li>

            </ul>
            <br>
            <br>
        </div>
        <div class="para1">
            <p><strong>WHICH SECTION OF FARMERS ARE COVERED IN THIS?</strong></p>
            <br>
            <ul>
                <li>All the farmers growing notified crops in a notified area during the season who have insurable in
                    the
                    crop are eligible.</li>
            </ul>
            <br>
            <br>
        </div>
        <div class="para1">
            <p><strong>COVERAGE OF RISKS AND EXCLUSIONS : </strong></p>
            <br>
            <ul>
                <li>Prevented Sowing/Planting/Germination Risk: Insured area is prevented from
                    sowing/planting/germination due to deficit rainfall or adverse seasonal/weather conditions.</li>
                <li>Standing Crop (Sowing to Harvesting): Comprehensive risk insurance is provided to cover yield losses
                    due to non-preventable risks, viz. Drought, Dry spell, Flood, Inundation, widespread Pests and
                    Disease attack, Landslides, Fire due to natural causes, Lightening, Storm, Hailstorm and Cyclone.
                </li>
                <li>Post-Harvest Losses: Coverage is available only up to a maximum period of two weeks from harvesting,
                    for those crops which are required to be dried in cut and spread / small bundled condition in the
                    field after harvesting against specific perils of Hailstorm, Cyclone, Cyclonic rains and Unseasonal
                    rains</li>
                <li>Localized Calamities: Loss/damage to notified insured crops resulting from occurrence of identified
                    localized risks of Hailstorm, Landslide, Inundation, Cloud burst and Natural fire due to lightening
                    affecting isolated farms in the notified area.</li>
                <li>Add-on coverage for crop loss due to attack by wild animals: The States may consider providing
                    add-on coverage for crop loss due to attack by wild animals wherever the risk is perceived to be
                    substantial and is identifiable.</li>
            </ul>
        </div>
        <br>
