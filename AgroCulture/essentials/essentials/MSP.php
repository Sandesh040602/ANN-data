        crop prices.</li>
      <li>The concept of the Minimum Support Price (MSP) system acts as a security to farmers so that their crops get
        the amount for their products and helps them sustain their losses, and does not affect them drastically.</li>
      <li>All farmers are assured at least the minimum price for their produce , MSP is the minimum price a farmer must
        be paid for their food grains as guaranteed by the government.</li>
