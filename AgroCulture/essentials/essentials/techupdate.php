            <p><strong>FEATURES OF DRONES IN AGRICULTURE : </strong></p>
            <br>
            <ol type="1">
                <li><strong>Irrigation Monitoring </strong>: Drones, including hyperspectral, thermal, or
                    multispectral sensors, recognize areas that are too dry or need improvement by the farmer. Drone
                    survey helps improve water efficiency and disclose potential pooling/leaks in irrigation by
                    providing Irrigation monitoring yields calculations of the vegetation index to help realize the
                    health of crops and emitted heat/energy.</li>
                <li><strong>Crop health monitoring </strong>: It is crucial to track the health of the vegetation
                    and spot bacterial/fungal plagues in the early stages. Agriculture drones can see which plants
                    reflect different amounts of green light and Near-infrared spectroscopy (NIRS) light. This data
                    helps produce multispectral images to track crop health. Quick monitoring and discoveries of any
                    defects can help save crops. In circumstances of crop failure, the farmer can also document the
                    damages for accurate insurance claims.
                </li>
                <li><strong>Crop damage assessment </strong>: Agricultural drones fitted along with multispectral
                    sensors and RGB sensors also detect field areas inflicted by weeds, infections, and pests.
                    According to this data, the exact amounts of chemicals needed to fight these infestations are
                    known, and this helps diminish the costs inflicted by the farmer.</li>
                <li><strong>Field Soil analysis </strong>: The drone survey allows farmers to obtain information
                    about their land's soil conditions. Multispectral sensors allow seizing data useful for seed
                    planting patterns, thorough field soil analysis, irrigation, and nitrogen-level management.
                    Precise Photogrammetry/ 3D mapping permits farmers to analyze their soil conditions thoroughly.
                </li>
                <li><strong>Livestock tracking </strong>: The drone survey allows the farmers not to keep track of
                    their crops only but also monitor the movements of their cattle. Thermal sensor technology helps
                    find lost animals and detect an injury or sickness. Drones can carry out this function
                    favorably, and this adds comprehensively to the production of vegetation.</li>
            </ol>
