            <p><strong>Application of IoT in Agriculture : </strong></p>
            <br>
            <ol type="1">
                <li>IoT-based smart farming is a system built to monitor the agricultural land with the help of sensors
                    (soil moisture, humidity, light, temperature, etc.) and automate the irrigation practices. The
                    farmers can observe the field conditions from anywhere. For example, it will alert the farmer when
                    the soil moisture level is low; the farmer can use sensors to initiate irrigation. IoT-based smart
                    farming is highly efficient in contrast to traditional methods.
                </li>
                <li>Precision farming is the most common application of IoT in farming. It makes the agricultural
                    practices more precise and well-planned by including processes like real-time crop and soil
                    condition monitoring, plant health tracking, and weather prediction. The farmers can administer
                    their fields based on the insights obtained by this system. </li>
                <li> the IoT-integrated Artificial Intelligence system can give up-to-date alerts and data to optimize
                    crop growth processes and to reduce yield loss because of pests, detrimental soil moisture level, or
                    weather damages. This farming method assures precise usage of resources and increases field
                    productivity while maintaining the sustainability of the process.</li>
                <li>IoT-equipped irrigation systems not only conserve water but also ensure that crops are getting just
                    the right amount of water for their optimum growth. This method of irrigation relies on the soil
                    moisture level rather than pre-determined interval-based irrigation.</li>

            </ol>
